// cada parrafo tiene su propia funcion
function funcion1(){
    let contenidooculto = document.getElementById('contenido-oculto');
    if (contenidooculto.style.display !== 'block') {
        contenidooculto.style.display = 'block';
        document.getElementById('mostrar-boton').innerText = 'Ocultar Contenido';
    } else {
        contenidooculto.style.display = 'none';
        document.getElementById('mostrar-boton').innerText = 'Mostrar Contenido';
    }
}

